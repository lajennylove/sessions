    <li>
        Vuelos redondos
    </li>
    <li>
        Viáticos (comida y transporte)
    </li>
</ul>
<p>
    *Los gastos personales corren por su cuenta
</p>
<p>
    6. ¿CÓMO PARTICIPAR?
</p>
<p>
A través de la plataforma <strong><em>sessions.vans.com.mx</em></strong>    <em> </em>de Vans México convocará a bandas independientes de la República
    Mexicana a crear un perfil para conseguir votos del público y poder
    calificar a las semifinales.
</p>
<p>
    Las votaciones serán recibidas en la plataforma del 3 de mayo al 13 de
    junio de 2018, periodo en el que las bandas podrán compartir el contenido
    de la plataforma para conseguir votos de seguidores.
</p>
<p>
    Las 16 bandas con más votos y seleccionadas por el jurado calificador,
    accederán a las semifinales las cuales se llevarán a cabo los días 28 y 30
    de junio y 6 y 7 de julio de 2018.
</p>
<p>
    La final se llevará a cabo el día 21 de julio de 2018 con las cuatro bandas
    ganadoras de cada semifinal y de la que se seleccionará una ganadora.
</p>
<p>
    7.CRITERIOS DE CALIFICACIÓN
</p>
<p>
    El criterio de calificación para elegir a las bandas que pasen a la fase de
    eliminatorias será el siguiente:
</p>
<p>
    -Votación: Las bandas que más votos acumulen en la plataforma en el periodo
    de tiempo arriba descrito.
</p>
<p>
    - Reproducciones: Se tomará en cuenta la cantidad de reproducciones en el
    perfil de la banda.
</p>
<p>
    - Se sumarán los 2 criterios anteriores.
</p>
<p>
    Vans se reservará el derecho de descalificar a las bandas que obtenga votos
    de manera ilícita o a través de boots así como bandas con mayor número de
    votos pero sin reproducciones.
</p>
<p>
    - Veredicto de jueces: Un jurado será el último filtro para elegir a las
    bandas más votadas con más calidad musical.
</p>
<p>
    8. SEMIFINALES
</p>
<p>
    - Las semifinales tendrán lugar durante el mes de junio y julio en el Foro
    Indie Rocks
</p>
<p>
    - Cuatro bandas se enfrentarán en cada semifinal de la que saldrá una
    finalista en cada fecha.
</p>
<p>
    - Cada semifinal contará con la presencia de tres jueces expertos en el
    ámbito musical que decidirán a la banda ganadora.
</p>
<p>
    - Las cuatro bandas semifinalistas se enfrentarán en una final el 21 de
    julio de la que saldrá una ganadora bajo el mismo criterio de jueceo.
</p>
<p>
    - Todas las bandas semifinalistas portarán ropa Vans que será suministrada
    por la marca por lo que no pueden tener relación con alguna otra marca. Su
    imagen será exclusiva de Vans por el periodo de participación y no podrán
    usar otras marcas.
</p>
<p>
    8. INDICACIONES DE HOUSE OF VANS SESSIONS
</p>
<p>
Para participar deberás inscribir a tu banda en la plataforma    <strong>sessions.vans.com.mx</strong> a partir del 3 de mayo y hasta el 13
    de junio de 2018. Las 30 bandas que reúnan más votos y escuchas en el
    periodo de tiempo pasarán a una pre selección donde un jurado calificador
    experto en la industria seleccionará 16 semifinalistas de las 30 bandas más
    votadas y escuchadas. Estas accederán a las semifinales en las cuales las
    bandas deberán presentarse en vivo para seguir avanzando en la contienda.
</p>
