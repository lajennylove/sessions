<p>
    2. COBERTURA
</p>
<p>
    Todos los estados de la República Mexicana.
</p>
<p>
    3. DURACIÓN DE LA DINÁMICA
</p>
<p>
    Del 3 de mayo de 2018 al 21 de julio de 2018.
</p>
<p>
    4. PARTICIPANTES
</p>
<p>
    Al participar estás aceptando las reglas de la dinámica y deberás
    suministrar la información solicitada, misma que será veraz, sin
    falsificación, suplantación de cuentas o perfiles dudosos.
</p>
<p>
    5. PREMIOS
</p>
<p>
    - La banda ganadora tendrán la oportunidad de presentarse en el escenario
    de House Of Vans México
</p>
<p>
    - La banda ganadora tendrán la oportunidad de ser parte del roster de
    bandas apoyadas por Vans México.
</p>
<p>
    - La banda ganadora viajará a alguna de las ediciones de House of Vans en
    el extranjero. (Estados Unidos, Europa o Sudamérica)
</p>
<p>
    El viaje incluye:
</p>
<ul>
    <li>
